public class Familia {
    private String title_family;
    private String description_family;

    public Familia(String title_family, String description_family) {
        this.title_family = title_family;
        this.description_family = description_family;
    }


    public String getTitle_family() {
        return title_family;
    }

    public void setTitle_family(String title_family) {
        this.title_family = title_family;
    }

    public String getDescription_family() {
        return description_family;
    }

    public void setDescription_family(String description_family) {
        this.description_family = description_family;
    }
}
