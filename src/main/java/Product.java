public class Product {
    private String title_product;
    private String image_product;
    private String description_product;
    private String title_design;
    private String title_producttype;

    public String getTitle_product() {
        return title_product;
    }

    public void setTitle_product(String title_product) {
        this.title_product = title_product;
    }

    public String getImage_product() {
        return image_product;
    }

    public void setImage_product(String image_product) {
        this.image_product = image_product;
    }

    public String getDescription_product() {
        return description_product;
    }

    public void setDescription_product(String description_product) {
        this.description_product = description_product;
    }

    public String getTitle_design() {
        return title_design;
    }

    public void setTitle_design(String title_design) {
        this.title_design = title_design;
    }

    public String getTitle_producttype() {
        return title_producttype;
    }

    public void setTitle_producttype(String title_producttype) {
        this.title_producttype = title_producttype;
    }
}
